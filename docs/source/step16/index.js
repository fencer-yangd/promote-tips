// 小数如何转化为二进制
// for example
// 0.125 -> 二进制： 乘2取整
// 0.125 * 2 = 0.5 -> 0
// 0.5 * 2 -> 1 -> 1
// 0.125 -> 01

// 0.3 -> 二进制
// 0.3 * 2 = 0.6 -> 0
// 0.6 * 2 = 1.2 -> 1
// 0.2 * 2 = 0.4 -> 0
// 0.4 * 2 = 0.8 -> 0
// 0.8 * 2 = 1.6 -> 1
// ...
// 0.3 -> 010011001...1001

// 0.2 -> 二进制
// 0.2 * 2 = 0.4 -> 0
// 0.4 * 2 = 0.8 -> 0
// 0.8 * 2 = 1.6 -> 1
// 0.6 * 2 = 1.2 -> 1
// 0.2 * 2 = 0.4 -> 0
// ...
// 0.2 -> 00110011...0011

// 0.3 - 0.2 !== 0.1
